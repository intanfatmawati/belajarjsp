    public void loadRecords2() {
        try {
            DBConnection conn =
            DBConnection.getInstance();
            kelasQuery daoKelas = new kelasQuery(conn.getCon());
            records = daoKelas.getAllKd(txtkd.getText());
        } catch (SQLException ex) {
        Logger.getLogger(formKelas.class.getName()).log(Level.SEVERE, null,
        ex);
        }
    }
    public void updateRow2() {
        if (this.records.size() > 0) {
            if (row < 0) {
            row = 0;
            }
        kelas kls = records.get(row);
        txtkd.setText(kls.getKd());
        txtjurusan.setText(kls.getJurusan());
        txtketua.setText(kls.getKetua());
        txtwali.setText(kls.getWali());
        txtjml.setText(kls.getJml());
        } else {
        JOptionPane.showMessageDialog(this, "Not Found");
        loadRecords();
        updateRow();
        }
    }
    
