    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        if (status == true) {
        try {
            kelas kls = new kelas();
            kls.setkd(txtkd.getText());
            kls.setJurusan(txtjurusan.getText());
            kls.setKetua(txtketua.getText());
            kls.setWali(txtwali.getText());
            kls.setJml(txtjml.getText());
            DBConnection conn = DBConnection.getInstance();
            kelasQuery dao = new kelasQuery(conn.getCon());
            dao.insert(kls);
            JOptionPane.showMessageDialog(this, "Data Saved");
            loadRecords();
            updateRow();
        } catch (SQLException ex) {
        Logger.getLogger(formKelas.class.getName()).log(Level.SEVERE, null, ex);
        }
        } else {
        try {
        kelas kls = new kelas();
        kls.setkd(txtkd.getText());
        kls.setJurusan(txtjurusan.getText());
        kls.setKetua(txtketua.getText());
        kls.setWali(txtwali.getText());
        kls.setJml(txtjml.getText());
        DBConnection conn = DBConnection.getInstance();
        kelasQuery dao = new kelasQuery(conn.getCon());
        dao.update(kls.getKd(), kls);
        JOptionPane.showMessageDialog(this, "Data Update");
        loadRecords();
        updateRow();
        } catch (SQLException ex) {
        Logger.getLogger(formKelas.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
    }//GEN-LAST:event_btnsaveActionPerformed

