    public void loadRecords() {
    try {
        DBConnection conn = DBConnection.getInstance();
        kelasQuery daoKelas = new kelasQuery(conn.getCon());
        records = daoKelas.getAll();
        } catch (SQLException ex) {
        Logger.getLogger(formKelas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    public void updateRow() {
        if (this.records.size() > 0) {
            if (row < 0) {
            row = 0;
            }
            kelas kls = records.get(row);
            txtkd.setText(kls.getKd());
            txtjurusan.setText(kls.getJurusan());
            txtketua.setText(kls.getKetua());
            txtwali.setText(kls.getWali());
            txtjml.setText(kls.getJml());
        } else {
        bersihkanForm();
        editForm(false);
        }
    }

